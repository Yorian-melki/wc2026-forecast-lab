def test_form_engine_compute_final_form():
    from wc2026.data_pipeline.form_engine import _compute_final_form
    # High Elo, positive momentum, good style → near 100
    high = _compute_final_form(2150, 150, 0.15, 0.9)
    assert high > 70, f"High form expected >70, got {high}"

    # Low Elo, negative momentum, poor style → near 0
    low = _compute_final_form(1420, -200, 0.05, 0.1)
    assert low < 30, f"Low form expected <30, got {low}"

    # Result must always be in [0, 100]
    for elo in [1400, 1600, 1800, 2000, 2200]:
        for d in [-300, 0, 300]:
            f = _compute_final_form(elo, d, 0.10, 0.5)
            assert 0 <= f <= 100, f"_compute_final_form({elo},{d}) = {f} out of [0,100]"


