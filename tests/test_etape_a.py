def test_elo_to_form_monotone_in_elo():
    """Higher Elo → higher (or equal) form, holding d1yr constant."""
    from wc2026.data_pipeline.elo_engine import elo_to_form
    scores = [elo_to_form(elo, 0) for elo in [1500, 1700, 1900, 2100]]
    for i in range(len(scores) - 1):
        assert scores[i] <= scores[i + 1], "form should not decrease with higher Elo"


def test_elo_to_form_monotone_in_momentum():
    """Higher d1yr (more momentum) → higher form, holding elo constant."""
    from wc2026.data_pipeline.elo_engine import elo_to_form
    scores = [elo_to_form(1900, d) for d in [-200, -100, 0, 100, 200]]
    for i in range(len(scores) - 1):
        assert scores[i] <= scores[i + 1], "form should not decrease with higher momentum"


