def test_brier_score_random_baseline():
    """Random model (uniform probabilities) Brier should equal analytical baseline."""
    from wc2026.data_pipeline.backtest import _brier_score, WC2022_ALL_TEAMS, WC2022_GROUP_SURVIVORS

    n = len(WC2022_ALL_TEAMS)  # 32
    n_pos = len(WC2022_GROUP_SURVIVORS)  # 16
    p = n_pos / n  # 0.5 for group survival
    probs_uniform = {t: p for t in WC2022_ALL_TEAMS}

    brier = _brier_score(probs_uniform, WC2022_GROUP_SURVIVORS, WC2022_ALL_TEAMS)
    # Analytical: n_pos * (p-1)² + n_neg * p² / n = p*(1-p) = 0.25 for p=0.5
    expected = p * (1 - p)
    assert abs(brier - expected) < 1e-6, f"Brier uniform={brier:.6f}, expected={expected:.6f}"


def test_brier_score_perfect_model():
    """A model assigning 100% to each correct team gets Brier = 0."""
    from wc2026.data_pipeline.backtest import _brier_score, WC2022_ALL_TEAMS, WC2022_GROUP_SURVIVORS

    perfect_probs = {
        t: (1.0 if t in WC2022_GROUP_SURVIVORS else 0.0)
        for t in WC2022_ALL_TEAMS
    }
    brier = _brier_score(perfect_probs, WC2022_GROUP_SURVIVORS, WC2022_ALL_TEAMS)
    assert brier == 0.0, f"Perfect model Brier should be 0, got {brier}"


def test_brier_score_group_survival_beats_random():
    """Our backtest result should beat the random baseline on group survival."""
    backtest_csv = ROOT / "outputs" / "backtest_wc2022.csv"
    if not backtest_csv.exists():
        pytest.skip("backtest_wc2022.csv not generated yet")

    from wc2026.data_pipeline.backtest import (
        _brier_score, WC2022_ALL_TEAMS, WC2022_GROUP_SURVIVORS,
    )
    df = pd.read_csv(backtest_csv)
    probs = dict(zip(df["team"], df["prob_group_survival"]))
    brier = _brier_score(probs, WC2022_GROUP_SURVIVORS, WC2022_ALL_TEAMS)

    random_baseline = 0.25  # p=0.5 group survival → Brier = 0.25
    assert brier < random_baseline, \
        f"Model Brier={brier:.4f} should beat random={random_baseline}"


